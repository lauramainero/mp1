placa = int(input())
digito = placa % 10
if digito == 0:
    print("0")
elif digito == 1:
    print("1")
elif digito == 2:
    print("2")
elif digito == 3:
    print("3")
elif digito == 4:
    print("4")
elif digito == 5:
    print("5")
elif digito == 6:
    print("6")
elif digito == 7:
    print("7")
elif digito == 8:
    print("8")
else: 
    print("9")